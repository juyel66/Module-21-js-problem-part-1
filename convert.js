// mile to KM convert
function milToLilometer(mile){
    const kilo= mile*1.609;
    return kilo;
}
const result = milToLilometer(5);
console.log(result);

// gram to kg convert

function gramToKg(gram){
    const KG = gram/1000;
    return KG;
}
const results = gramToKg(5000);
console.log(results);
